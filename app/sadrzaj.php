       <h3>Voda i trening</h3>

Voda je najbitniji sastojak za zdrav život, a naše telo sadrži čak 65 odsto vode. Neuzimanjem vode možemo izgubiti i do 20 odsto snage i energije koja nam je potrebna za trening. Osim toga, znojenjem se gubi velika količine vode iz organizma.

- Preporučljivo je da se uzima od dva do četiri litra tečnosti dnevno, a tu, osim vode, spadaju i sokovi i drugi napici. Nije dobro popiti previše vode pre treninga jer onda ona ubrzava izbacivanje minerala iz organizma. Tokom treninga treba uzimati umerenu količinu vode jer tokom vežbanja telo dehidrira.<br><hr>

<h3>Magnezijum</h3><br>
Od svih minerala magnezijum je možda najvažniji za sportiste, jer je bez njega nemoguća stabilnost i funkcija ATP-a. istraživanja su pokazala da dodatak od od 310 do 320 mg za žene tj. 400 do 420 mg za muškarce magnezijuma dnevno, znatno povećava snagu sportiste, elastičnost mišića i ubrzava oporavak mišićnog sistema. Magnezijum deluje direktno na neuromišićni spoj (neuromišićnu ploču), a potpomaže iskorišćavanje lipida, proteina i ugljenih hidrata. Neophodan je u stanjima umora i napora, nervoze i napetosti.<br><hr>


  <h3>Ishrana pre treninga<br></h3>
Ishrana pre treninga treba da sadrži lako svarljive namirnice koje će tokom fizičke aktivnosti doprineti zadovoljavanju energetskih  potreba i potreba za tečnošću. Takav obrok treba da bude sa visokim sadržajem ugljenih hidrata i sa relativno malo masti i belančevina. Pravilna ishrana pre fizičke aktivnosti ima višestruku ulogu. Ona treba da: Spreči nizak nivo šećera u krvi tokom fizičke aktivnosti, obezbedi energiju korišćenjem uskladištenog glikogena u mišićima, umiri stomak, apsorbuje želudačne sokove i spreči glad, ulije sigurnost sportisti u njegove fizičke sposobnosti. Ishrana ugljenimhidratima sa visokim glikemijskim indeksom (čokolada, beli šećer,…) u toku jednog sata pre fizičke aktivnosti, može negativno da utiče na takmičarsku sposobnost, jer brži rast glukoze u krvi uzrokuje povećano oslobađanje insulina, što dovodi do relativne hipoglikemije i inhibiše mobilizaciju masti iz masnog tkiva. To sve zajedno utiče na rano pražnjenje rezervi ugljenih hidrata. Suprotno tome, unos ugljenih hidrata sa niskimglikemijskim indeksom (žitarice, pasta, pirinač…) neće izazvati ove efekte, a pored toga obezbediće ravnomerni dotok glukoze iz digestivnog trakta tokom trajanja fizičke aktivnosti. Zato se pre fizičke aktivnosti preporučuju namirnice sa niskim glikemijskim indeksom kao što su jabuka, kruška, čokoladno mleko, nisko kalorični voćni jogurt, obrano mleko, kajsija sušena, sočivo, ječam, grejpfrut.<br><hr>


<h3>Ishrana nakon treninga i u toku oporavka<br></h3>
Odmah nakon treninga, u prvih 15 minuta, bilo bi poželjno da uzmete lagani obrok. To može biti banana, keks i-ili čokoladno mleko. Uzimanje hrane odmah nakon treninga će skratiti vreme koje je potrebno organizmu za odmor. Nakon dugotrajnog ili intezivnog treninga, najbitniji nutritivni prioritet je rehidracija, nakon koje treba odmah
nadoknaditi zalihe ugljenih hidrata. U prva dva sata nakon fizičke aktivnosti preporučen unos ugljenih hidrata je 0,7 do 1,5 g/kg telesne mase, a nakon toga 0,7 do 1 g/kg telesne mase na sat. Novija istraživanja pokazuju da su tokom perioda oporavka ugljeni hidrati sa visokim ili srednjim glikemijskim indeksom (SREDNJI: sok od pomorandže, kokice, integralni pirinač, pečeni pasulj, špagete VISOKI: beli šećer, pečeni krompir, korni-fleks, med, beli hleb, suvo groždje) i mala količina belancevina najefikasniji metod nadoknade glikogena u mišićima.<br><hr>
